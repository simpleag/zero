package model;

public class People {
	private String useId = "";
	private String openId = "";
	private String useNum = "";
	private String usePass = "";
	private String useName = "";
	private String useSchool = "";
	private String useTab = "";
	
	public String getUseId() {
		return useId;
	}
	public void setUseId(String useId) {
		this.useId = useId;
	}
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	public String getUseNum() {
		return useNum;
	}
	public void setUseNum(String useNum) {
		this.useNum = useNum;
	}
	public String getUsePass() {
		return usePass;
	}
	public void setUsePass(String usePass) {
		this.usePass = usePass;
	}
	public String getUseName() {
		return useName;
	}
	public void setUseName(String useName) {
		this.useName = useName;
	}
	public String getUseSchool() {
		return useSchool;
	}
	public void setUseSchool(String useSchool) {
		this.useSchool = useSchool;
	}
	public String getUseTab() {
		return useTab;
	}
	public void setUseTab(String useTab) {
		this.useTab = useTab;
	}
	
}
